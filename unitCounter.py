import battlecode as bc 

class unitCounter:
    currentFactories = 0
    currentRockets = 0
    currentWorkers = 0
    currentKnights = 0
    currentRangers = 0
    currentMages = 0
    currentHealers = 0

    def resetCount(self):
        self.currentFactories = 0
        self.currentRockets = 0
        self.currentWorkers = 0
        self.currentKnights = 0
        self.currentRangers = 0
        self.currentMages = 0
        self.currentHealers = 0